<h1 style="font-size:150%; text-transform:uppercase;">Política de Privacidade do P2 Win</h1>
<small class="text-muted">Em vigor a partir de 15 de julho de 2020</small>

<h2 style="line-height: 32px; margin-bottom: 20px;">Você está nos confiando suas informações quando usa nossos Serviços. Por esse motivo, trabalhamos arduamente para proteger suas informações.</h2>

<p>
Esta Política de Privacidade descreve como a IJJI, INC. (“P2 Win” ou “nós”) coleta, armazena, usa e compartilha informações sobre os usuários de nossos Serviços (conforme definido em nossos Termos de Uso). Esta Política de Privacidade (esta “Política de Privacidade”) está incorporada e está sujeita aos nossos Termos de Uso. Ao utilizar os Serviços, você concorda com a coleta, armazenamento, uso e divulgação de suas informações, conforme descrito nesta Política de Privacidade. Se você discordar de alguma coisa nesta Política de Privacidade, não use os Serviços.
</p>

<h2 style="altura da linha: 32px; margem inferior: 20px;">1. Quais informações coletamos</h2>

<p>
Coletamos informações para que possamos oferecer a melhor experiência possível quando você usa nossos Serviços. Muito do que você provavelmente considera dados pessoais é coletado diretamente de você quando você:
</p>

<ul>
   <li>Crie uma conta em nossos Serviços (por exemplo, endereço de e-mail, número de telefone);</li>
   <li>Fazer uma compra (por exemplo, informações de cobrança, incluindo nome, endereço, número de cartão de crédito);</li>
   <li>Solicite assistência da nossa equipe de suporte ao cliente (por exemplo, informações da conta, capturas de tela que você enviou, identificação governamental, quando aplicável); e</li>
   <li>Participe de concursos e pesquisas ou de outra forma participe de atividades que promovemos e que possam exigir informações sobre você.</li>
</ul>

<p>
No entanto, também coletamos informações adicionais quando você usa nossos Serviços para fornecer a experiência ideal. Usamos diversas tecnologias para coletar e armazenar informações, incluindo cookies, pixel tags, armazenamento local, como armazenamento na web do navegador ou caches de dados de aplicativos, bancos de dados e logs de servidor.
</p>

<p>
Esses métodos de coleta podem não ser tão óbvios para você, por isso destacamos e explicamos abaixo um pouco mais sobre o que podem ser (já que variam de tempos em tempos) e como funcionam.
</p>
 
<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Seus navegadores e dispositivos</h3>

<p>
Coletamos informações sobre os navegadores e dispositivos que você usa para acessar nossos Serviços, o que nos ajuda a solucionar bugs ou problemas que você possa enfrentar e a proteger sua conta contra acesso não autorizado.
</p>

<p>
As informações que coletamos incluem identificadores exclusivos, informações do navegador, informações do dispositivo, sistema operacional, informações da rede móvel, incluindo nome da operadora e número da versão do aplicativo. Também coletamos informações sobre a interação de seus navegadores e dispositivos com nossos Serviços, incluindo sua localização (endereço IP), data, hora e URL de referência de sua solicitação.
</p>

<p>
Coletamos essas informações quando você usa nossos Serviços — por exemplo, quando você faz login em nossos Serviços, as informações sobre seu navegador ou dispositivo são analisadas para evitar acesso não autorizado à sua conta.
</p>
 
<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Your activity</h3>

<p>
Coletamos informações sobre sua atividade em nossos Serviços, que usamos para fazer coisas como recomendar uma listagem que você possa gostar ou ajudá-lo com uma compra. As informações de atividade que coletamos podem incluir:
</p><p>

</p><ul>
   <li>Termos que você pesquisa;
   </li><li>Listagens que você visualiza;
   </li><li>Atividade de compra; e
   </li><li>Mensagens entre você e outro comprador ou vendedor
</li></ul>
 
 
<h2 style="altura da linha: 32px; margem inferior: 20px;">2. Como usamos as informações</h2>

<p>
Acreditamos firmemente na minimização dos dados que coletamos e na limitação de seu uso e finalidade apenas (1) àqueles para os quais recebemos permissão, (2) conforme necessário para fornecer os Serviços ou (3) conforme nos for solicitado ou permitido para conformidade legal ou outros fins legais. Nossos usos incluem:
</p>

<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Fornecer, atualizar e melhorar nossos Serviços</h3>
<p>
Coletamos diversas informações relacionadas ao seu uso e/ou interações com nossos Serviços. Utilizamos essas informações para:
</p>
<ul>
<li>Criar, desenvolver, operar, fornecer e melhorar nossos Serviços; </li>
   <li>Diagnosticar problemas e identificar quaisquer riscos de segurança, erros ou melhorias necessárias em nossos Serviços;</li>
   <li>Detectar e prevenir fraudes e abusos de nossos Serviços e sistemas;</li>
   <li>Gerar estatísticas agregadas sobre o uso de nossos Serviços; e</li>
   <li>Entenda e analise como você usa nossos Serviços e quais produtos e Serviços são mais relevantes para você.</li>
</ul>

<p>
Muitas vezes, muitos dos dados recolhidos são dados agregados ou estatísticos sobre como os indivíduos utilizam os nossos Serviços e não estão ligados a quaisquer dados pessoais. No entanto, na medida em que se trate de dados pessoais, ou estejam vinculados ou passíveis de ligação a dados pessoais, tratamo-los em conformidade.
</p>
 
<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Comunicando com você</h3>

<p>
Poderemos contatá-lo diretamente ou por meio de um provedor de serviços terceirizado em relação aos nossos Serviços, como para fornecer comunicações transacionais ou relacionadas a serviços. Também poderemos contatá-lo com ofertas de Serviços adicionais que achamos que você considerará valiosos se nos der consentimento ou quando permitido com base em interesses legítimos. Você não precisa fornecer consentimento como condição para usar nossos Serviços. Esses contatos podem incluir:
</p>
<ul>
   <li>E-mail;</li>
   <li>Notificação push de aplicativos móveis;</li>
   <li>Mensagens de texto (SMS); e</li>
   <li>Chamadas telefônicas.</li>
</ul>

<p>
Você também pode atualizar suas preferências em relação ao recebimento de comunicações nossas e/ou de nossos parceiros, acessando sua conta e visitando a página "Configurações" da conta.
</p>
 
<h2 style="line-height: 32px; margin-bottom: 20px;">3. Quando compartilhamos suas informações</h2>
<p>
Não compartilhamos suas informações pessoais com empresas, organizações ou indivíduos fora da P2 Win, exceto nos seguintes casos:
</p>
 
<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Com seu consentimento</h3>
<p>
Compartilharemos informações pessoais fora do P2 Win quando tivermos o seu consentimento. Pediremos seu consentimento explícito para compartilhar qualquer informação pessoal sensível.
</p>

<p>
Observe que, ao fazer transações com outro usuário, você concorda com o compartilhamento de suas informações com esse outro usuário, conforme necessário para concluir a transação, incluindo, sem limitação, seu endereço de entrega e/ou informações de pagamento.
</p>
 
<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Para processamento externo com terceiros confiáveis</h3>

<p>
Poderemos compartilhar seus dados pessoais com terceiros com os quais temos parceria para permitir que você integre seus serviços em nossos próprios Serviços, e com prestadores de serviços terceirizados confiáveis, conforme necessário para que eles executem serviços em nosso nome, tais como:
</p><p>
</p><ul>
   <li>Processar pagamentos com cartão de crédito;</li>
   <li>Comunicar-se com você, por exemplo, por e-mail;</li>
   <li>Realização de concursos ou pesquisas; e</li>
   <li>Fornecer suporte ao cliente.</li>
</ul>

<p>
Compartilhamos seus dados pessoais apenas conforme necessário para que terceiros forneçam os Serviços conforme solicitado ou necessário em nosso nome. Esses terceiros (e quaisquer subcontratados) estão sujeitos a rigorosos termos e condições de processamento de dados e estão proibidos de utilizar, compartilhar ou reter seus dados pessoais para qualquer finalidade que não aquela para a qual foram especificamente contratados (ou sem o seu consentimento).
</p>
 
<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Para conformidade com solicitações legais, regulatórias e de aplicação da lei</h3>

<p>
Ocasionalmente, pode ser necessário – por lei, processo legal, litígio e/ou solicitações de autoridades governamentais e policiais dentro ou fora do seu país de residência – que divulguemos suas informações pessoais. Também poderemos divulgar informações sobre você se determinarmos que é necessário ou apropriado para responder a reclamações e processos legais (como solicitações de intimação), para proteger a segurança do público ou de qualquer pessoa, ou para prevenir ou interromper qualquer atividade ilegal.
</p>
<p>
Na medida em que formos legalmente autorizados a fazê-lo, tomaremos medidas razoáveis para notificá-lo caso sejamos obrigados a fornecer suas informações pessoais a terceiros como parte de um processo legal.
</p>
 
<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Analise e meça o desempenho</h3>

<p>
Usamos dados para análise e medição para entender como nossos Serviços são usados. Por exemplo, analisamos dados sobre as suas visitas aos nossos sites para fazer coisas como otimizar o design do produto. Usamos uma variedade de ferramentas para fazer isso, incluindo Google Analytics e Facebook Analytics. Estas ferramentas utilizam técnicas como cookies de navegador para identificá-lo como um usuário único na próxima vez que visitar nosso site. Cada cookie não pode ser usado por ninguém que não seja o provedor de serviços (por exemplo, Google para Google Analytics). Embora as informações recolhidas não incluam dados pessoais como nome, endereço, informações de faturação, etc., as informações recolhidas são utilizadas e partilhadas por estes prestadores de serviços de acordo com as suas políticas de privacidade individuais.
</p>

<h3 style="line-height: 28px; margin-bottom: 15px; font-weight:400">Sites de terceiros</h3>
<p>
Nosso site e nossos aplicativos móveis podem conter links para sites de terceiros. Não somos responsáveis pelas práticas de privacidade ou pelo conteúdo de sites de terceiros. Por favor, leia a política de privacidade de qualquer site que você visitar.
</p>
 
<h2 style="altura da linha: 32px; margem inferior: 20px;">4. Como protegemos suas informações</h2>

<p>
Construímos nossos Serviços com fortes recursos de segurança que protegem continuamente suas informações. Monitoramos constantemente e bloqueamos automaticamente ameaças à segurança. Se detectarmos algo arriscado que achamos que você deveria saber, iremos notificá-lo e ajudaremos a orientá-lo nas etapas para se manter melhor protegido.
</p>

<p>
Trabalhamos arduamente para proteger você e nossos Serviços contra acesso não autorizado, alteração, exposição ou destruição das informações que possuímos. Entre outras coisas, nós:
</p>

<ul>
   <li>Use criptografia para manter seus dados privados durante o trânsito;</li>
   <li>Inclua recursos de segurança como a verificação em duas etapas para ajudar você a proteger sua conta;</li>
   <li>Revisar e atualizar nossas práticas de coleta, armazenamento e processamento de informações para evitar acesso não autorizado aos nossos sistemas; e</li>
   <li>Restringir o acesso às informações pessoais aos funcionários, contratados e agentes da P2 Win que precisam dessas informações para fornecer nossos Serviços, e qualquer pessoa com esse acesso está sujeita a rigorosas obrigações contratuais de confidencialidade e pode ser disciplinada ou demitida se não cumprir essas obrigações. obrigações.</li>
</ul>
 
<h2 style="altura da linha: 32px; margem inferior: 20px;">5. Você pode acessar, atualizar ou excluir seus dados</h2>

<p>
