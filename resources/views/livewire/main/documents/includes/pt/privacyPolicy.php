</p>

<p>
Se você fizer uma solicitação para excluir seus dados pessoais, a solicitação será atendida de acordo com as leis aplicáveis ou, de outra forma, na medida em que não seja mais necessária para quaisquer Serviços ou exigida para nossos fins comerciais legítimos ou requisitos legais ou contratuais de manutenção de registros.
</p>

<p>
Retemos dados pessoais apenas pelo tempo necessário para fornecer os Serviços que você solicitou e, posteriormente, para diversos fins legais ou comerciais legítimos. Isso pode incluir períodos de retenção:
</p>

<ul>
   <li>obrigado por lei, contrato ou obrigações semelhantes aplicáveis às nossas operações comerciais;</li>
   <li>para preservar, resolver, defender ou fazer cumprir os nossos direitos legais/contratuais; ou</li>
   <li>necessário para manter registros comerciais e financeiros adequados e precisos.</li>

</ul>

<p>
Poderemos recusar-nos a processar pedidos que sejam frívolos/vexatórios, que ponham em risco a privacidade de terceiros, que sejam extremamente impraticáveis ou para os quais o acesso não seja de outra forma exigido pela legislação local. Solicitações de acesso, correção ou exclusão podem ser feitas <a href="https://P2 Win.zendesk.com/hc/en-us/requests/new">entrando em contato com nosso Suporte ao Cliente</a>.
</p>
 
