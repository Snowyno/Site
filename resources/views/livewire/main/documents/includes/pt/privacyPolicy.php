</p>

<p>
Você pode se recusar a compartilhar determinados Dados Pessoais conosco; nesse caso, talvez não possamos fornecer a você alguns dos recursos e funcionalidades dos Serviços.
</p>

<p>
A qualquer momento, poderá opor-se ao tratamento dos seus Dados Pessoais, por motivos legítimos, exceto se de outra forma permitido pela lei aplicável. Se você acredita que seu direito à privacidade concedido pelas leis de proteção de dados aplicáveis foi infringido, entre em contato conosco conforme estabelecido na seção “Como entrar em contato conosco” na Política de Privacidade. Você também tem o direito de apresentar uma reclamação às autoridades de proteção de dados.
</p>

<h3 style="line-height: 28px; margin-bottom: 1.5em; font-weight:400">Base legal para processamento de Dados Pessoais; retenção</h3>

<p>
Somente coletaremos e processaremos Dados Pessoais quando tivermos bases legais para fazê-lo. Estas bases legais incluem quando você dá consentimento, quando temos uma obrigação contratual de coletar ou processar seus Dados Pessoais e quando temos um interesse legítimo em processar seus Dados Pessoais.
</p>

<p>
Quando recolhemos Dados Pessoais, conservamo-los durante o tempo que for necessário para a finalidade para a qual estão a ser tratados. Por exemplo, reteremos suas informações de e-mail enquanto os Serviços estiverem ativos ou conforme necessário para fornecer os Serviços a você. Depois disso, manteremos os Dados Pessoais por um período que nos permita tratar ou responder a quaisquer reclamações, dúvidas ou preocupações relacionadas com a sua utilização dos Produtos. Retemos os dados identificáveis que coletamos diretamente para fins de segmentação pelo menor tempo possível, após o qual empregamos medidas para excluir permanentemente, anonimizar ou desidentificar os dados. Revisaremos periodicamente os Dados Pessoais que mantemos e os excluiremos de forma segura quando não houver mais necessidade legal, comercial ou do consumidor de mantê-los.
</p>

