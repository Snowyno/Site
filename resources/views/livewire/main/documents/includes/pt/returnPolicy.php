<h2>Política de Devolução</h2>

<seção class="article-info">
         <div class="artigo-content">
           <div class="article-body"><p>Todas as vendas realizadas no “site” P2Win representam o processo final de intermediação entre o vendedor e o comprador do produto, não cabendo dessa forma, reembolso ou devolução do mesmo. Esse processo só será previsto se o item recebido não estiver funcionando ou esteja em desacordo com o que foi descrito. Se for esse o caso, o adquirente estará coberto pela Garantia de comprador P2Win.</a>.</p>
<p>A transação é considerada concluída com a entrega efetiva do produto e a confirmação de seu recebimento pelo agente comprador, essa confirmação pode ser realizada a qualquer momento após a entrega do produto. Passadas 72 horas da entrega do produto, esse será considerado entregue e o processo de compra finalizado independente da confirmação do recebimento, não cabendo em nenhuma hipótese a solicitação de devolução ou reembolso. </p>
<p>Para evitar disputas em uma transação provenientes de falha de entendimento em relação ao produto oferecido, aconselhamos aos vendedores a clareza e profundidade na descrição do item, detalhando o quanto possível ao criar listagens. Incentivamos os compradores a revisarem as fotos da listagem, a descrição, a classificação do vendedor e a condição do item antes de efetuar a compra. No caso de qualquer dúvida sobre a listagem, peça informações adicionais ao vendedor.</p>
<p></p>
<p><strong>As devoluções estão sujeitas às seguintes diretrizes:</strong></p>
<ul>
<li><span>•	A partir da verificação por parte do comprador de alguma irregularidade no produto, o mesmo deverá acessar o sistema e abrir uma disputa para a apresentação do problema e eventual correção do mesmo. </span></li>
<li><span>•	Caso confirmada a não funcionalidade do produto ou qualquer outro problema na entrega do mesmo os valores efetuados na compra serão estornados, desde que o comprador não tenha registrado a confirmação do produto. </span></li>
</ul>
<p><strong>Reivindicações fraudulentas</strong></p>
<p>Reivindicações fraudulentas podem incluir:</p>
<ul>
<li>Um comprador solicita um estorno por meio do processador de pagamento após receber o item comprado</li>
<li>Um comprador solicitando um estorno por meio do processador de pagamento após avaliar e concluir a compra</li>
</ul>
<p> Os compradores que apresentarem reclamações fraudulentas estão sujeitos a consequências e, além disso, reservamo-nos o direito de suspender permanentemente a conta do comprador ao abrigo da política de devolução.</p>

