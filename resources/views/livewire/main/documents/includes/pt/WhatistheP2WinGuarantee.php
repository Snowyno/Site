<h2>O que é a Garantia P2Win?</h2>
<seção class="article-info">
         <div class="artigo-content">
           <div class="article-body"><p>Todas as compras feitas no P2Win são protegidas gratuitamente e nossa garantia P2Win garante que você receba o item conforme descrito ou seu dinheiro de volta.</p>
<p>Os compradores podem usar a Garantia P2Win quando:</p>
<ul>
<li><span>Eles não recebem um item</span></li>
<li><span>Eles recebem um item que não funciona</span></li>
<li><span>Eles recebem um item que não corresponde à descrição do anúncio</span></li>
<li><span>Eles não recebem uma sessão do Play with Pro</span></li>
<li><span>Eles não recebem uma sessão criativa</span></li>
</ul>
<p>A maioria dos vendedores trabalha com os compradores para resolver problemas rapidamente, mas se uma solução não for alcançada, nós podemos ajudar.</p>
<p><span class="im"><strong>NÃO</strong> conclua a transação se você recebeu um item que não está funcionando ou não conforme descrito. Ao concluir a transação, você aceita o item e finaliza a transação. Depois que uma transação for finalizada e concluída, não haverá absolutamente nenhuma devolução ou reembolso.<br><br>Faça o seguinte se o item entregue não estiver conforme descrito ou funcionando:<br></span></p>
<ol>
<li>Entre em contato com o vendedor e informe-o sobre o problema, o que aconteceu e os meios de devolução do item e/ou sobre outro resultado que você e o vendedor concordem.</li>
<li>Em seguida, acesse a listagem em questão e pressione o link <strong>"Tenho um problema com o item que recebi"</strong>. Isso criará uma seção suspensa na qual você precisará especificar o problema que está enfrentando, seguido de uma breve explicação da situação.</li>
<li>Isso pausará a contagem regressiva de preenchimento automático de 3 dias e permitirá que o suporte da P2Win analise seu caso e ajude você na resolução da transação. Se precisarmos de mais informações para validar sua reivindicação, enviaremos um e-mail solicitando informações específicas para fazê-lo. Assim que pudermos verificar se o item se enquadra na nossa Garantia do Comprador P2Win ou em qualquer outra resolução entre você e o Vendedor, autorizaremos o reembolso da compra.</li>
</ol>
<p><strong>O que não está coberto</strong></p>
<ul>
<li>Compradores que concluíram a transação, concordando em receber e verificar o item.</li>
<li>Remorso do comprador ou qualquer motivo que não seja não receber um item, receber um item que não seja conforme descrito na listagem ou não funcionar (consulte nosso <a href="/hc/en-us/articles/206128647" >política de devolução</a> para mais informações).</li>
<li>Itens danificados durante a coleta ou envio ou não entregues quando o comprador organiza a coleta ou envio do item (por exemplo, o comprador organiza o frete).</li>
<li>Todos os itens garantidos (negociações de bot) que são <em>entregues</em> não são cobertos pela Garantia do Comprador P2Win, pois são garantidos como descritos.</li>
<li>As transações realizadas fora da nossa plataforma são estritamente proibidas pelos nossos <a href="https://P2Win.com/about/terms" target="_blank" rel="noopener">Termos de Uso</a>&nbsp ;e não será coberto pela nossa Garantia de Proteção ao Comprador.</li>
</ul>
<p><strong>Reembolsos aos compradores</strong></p>
<p>Todos os reembolsos serão feitos no valor exato e na forma de pagamento em que a transação original foi processada. Consulte o seguinte <a href="/hc/en-us/articles/206156308">artigo de perguntas frequentes</a> para obter mais informações sobre como funcionam os reembolsos.</p>
<p><strong>Reivindicações fraudulentas</strong></p>
<p>Reivindicações fraudulentas podem incluir:</p>
<ul>
<li>Um comprador solicita um estorno após receber o item comprado</li>
<li><span>Um comprador solicitando um estorno após receber um reembolso conscientemente</span></li>
<li>Um comprador solicitando um estorno após concluir a compra</li>
</ul>
<p>Os compradores que apresentarem reclamações fraudulentas estão sujeitos a consequências e, além disso, reservamo-nos o direito de suspender permanentemente a conta do comprador ao abrigo da Garantia P2Win.</p>
<p><strong>Outros termos</strong></p>
<ul>
<li><span>O comprador terá 3 dias a partir da marcação do item como recebido para enviar uma disputa sob a garantia P2Win.</span></li>
<li>Se não recebermos nenhuma informação solicitada dentro de 48 horas úteis do Comprador, a reclamação poderá ser negada e a Garantia P2Win perdida como resultado de não fornecermos evidências suficientes para validar a disputa. Não responder a nenhum e-mail dentro do prazo indicado também pode considerar que o problema foi resolvido e pode resultar na resolução da disputa.</li>
<li><span>Compradores e vendedores nos permitem tomar decisões finais sobre todos os casos, incluindo recursos.</span></li>
<li><span>Podemos fornecer aos compradores e vendedores acesso aos nomes uns dos outros e às informações de contato relacionadas a uma solicitação.</span></li>
<li><span>A Garantia P2Win não é uma garantia do produto.&nbsp;</span></li>
</ul>
