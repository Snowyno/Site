<h2>What is the P2Win Guarantee?</h2>
<section class="article-info">
        <div class="article-content">
          <div class="article-body"><p>All purchases made on P2Win are protected for free, and&nbsp;our&nbsp;P2Win Guarantee ensures that you get the item as described or your money back.</p>
<p>Buyers can use the P2Win Guarantee when:</p>
<ul>
<li><span>They don't receive an item</span></li>
<li><span>They receive an item that does not work</span></li>
<li><span>They receive an item that doesn't match the listing description</span></li>
<li><span>They don't receive a Play with Pro session</span></li>
<li><span>They don't receive a Creative session</span></li>
</ul>
<p>Most sellers work with buyers to quickly resolve issues, but if a solution isn't reached, we can help.</p>
<p><span class="im"><strong>DO NOT</strong> complete the transaction if you have received an item that is not working or not as described. By completing the transaction, you are accepting the item and finalizing the transaction. Once a transaction has been finalized and completed, there are absolutely no returns or refunds.<br><br>Please do the following if the delivered item is not as described or working:<br></span></p>
<ol>
<li>Contact the Seller and let them know the issue, what happened and the means of returning the item, and/or on another outcome that you and the seller agree upon.</li>
<li>Then go to the listing in question and press the <strong>"I&nbsp;have an issue with the item I&nbsp;received"</strong>&nbsp;link. This will create a drop-down section in which you will need to specify the issue you are having, followed by a brief explanation of the situation.</li>
<li>This will pause the 3-day auto-completion countdown timer and allow P2Win support to review your case and assist you in the resolution of the transaction. If we need more information to validate your claim, we'll&nbsp;email&nbsp;you asking for specific information to do so. Once we are able to verify that the item falls under our P2Win Buyer Guarantee or any other resolution between you and the Seller, we'll authorize the refund of the purchase.</li>
</ol>
<p><strong>What is not covered</strong></p>
<ul>
<li>Buyers that have completed the transaction, agreeing to have received and verified the item.</li>
<li>Buyer's remorse or any reason other than not receiving an item, receiving an item that isn't as described in the listing, or not working (see our <a href="/hc/en-us/articles/206128647">return policy</a> for more info).</li>
<li>Items damaged during pick-up or shipping or not delivered when the buyer arranges pick-up or shipping of the item (for instance, the buyer arranges freight).</li>
<li>All escrowed items (Bot trades) that are <em>delivered</em> are not covered under the P2Win Buyer Guarantee as they're guaranteed to be as described.</li>
<li>Transactions performed outside of our platform are strictly prohibited by our <a href="https://P2Win.com/about/terms" target="_blank" rel="noopener">Terms of Use</a>&nbsp;and will not be covered by our Buyer Protection Guarantee.</li>
</ul>
<p><strong>Refunds to buyers</strong></p>
<p>All&nbsp;refunds will be made in the exact amount and payment method the original transaction was processed. Please see the following <a href="/hc/en-us/articles/206156308">FAQ article</a>&nbsp;for more information on how refunds work.</p>
<p><strong>Fraudulent claims</strong></p>
<p>Fraudulent claims may include:</p>
<ul>
<li>A buyer filing a chargeback after receiving&nbsp;their purchased item</li>
<li><span>A buyer filing a chargeback after knowingly receiving a refund</span></li>
<li>A buyer filing a chargeback after completing the purchase</li>
</ul>
<p>Buyers who file fraudulent claims are subject to consequences and in addition, we reserve the right to permanently&nbsp;suspend the buyer's account under the P2Win Guarantee.</p>
<p><strong>Other terms</strong></p>
<ul>
<li><span>The Buyer will have 3 days from&nbsp;the item being marked as received to submit a dispute&nbsp;under the P2Win Guarantee.</span></li>
<li>If we do not receive any requested information within 48 business hours from the Buyer, the claim can be denied and the P2Win Guarantee forfeited as the result of not providing enough evidence to validate the dispute. Not replying to any email within the noted time frame can also deem that the issue has been resolved and can result in the dispute being lifted.</li>
<li><span>Buyers and sellers permit us to make final decisions about all cases, including appeals.</span></li>
<li><span>We may provide buyers and sellers with access to each other's names, and contact information relating to a request.</span></li>
<li><span>The P2Win Guarantee is not a product warranty.&nbsp;</span></li>
</ul>
